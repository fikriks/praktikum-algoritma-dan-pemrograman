/* Penggunaan Format Data */
#include<iostream>
using namespace std;
main()
{
int a =20,b =30;//tipe data integer
float c =3.14;//tipe data float
char huruf = 'R';//tipe data character

//cara menggunakan format data untuk menampilkan
printf("%d\n",a);
cout<<"tampilkan nilai variabel b = "<<b;//tidak menggunakan format data
printf("\n%i\n",b);
printf("%f\n",c);
printf("%c\n",huruf);
return 0;
}
